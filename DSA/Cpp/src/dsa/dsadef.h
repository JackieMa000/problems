#ifndef DSA_SRC_DSA_DSADEF_H_
#define DSA_SRC_DSA_DSADEF_H_

namespace dsa {

// The unsigned integer type of the result of the sizeof operator.
typedef unsigned long size_t;

// Length of array, LinkedList, Tree, Queue, Stack. Represents how many nodes inside.
typedef unsigned long length_t;

// Depth of tree, represents how many levels in a tree
typedef unsigned int depth_t;

// Position of a node in an array, LinkedList, Tree.
typedef unsigned int pos_t;

}

#endif //DSA_SRC_DSA_DSADEF_H_
